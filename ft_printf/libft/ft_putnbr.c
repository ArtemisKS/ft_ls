#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int u_n;

	u_n = n;
	if (n < 0)
	{
		ft_putchar('-');
		u_n *= (-1);
	}
	if (u_n > 9)
	{
		ft_putnbr(u_n / 10);
	}
	ft_putchar(u_n % 10 + '0');
}
