int		ft_fcd(int a, int b)
{
	int	i;

	if (a <= 0 || b <= 0)
		return (0);
	i = (a >= b) ? a : b;
	while (--i)
		if ((a % i) == 0 && (b % i) == 0)
			return (i);
	return (1);
}
